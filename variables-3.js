let nombre = "Pepe"; // declarar una variable que se llama 'nombre' y le asigno un valor de tipo string (o cadena en texto);

// a partir de ahora la variable 'nombre' se dice que es de tipo string

// typeof es una palabra reservada que nos permite conocer el tipo que es una variable 
console.log("¿De qué tipo es la variable nombre?: ", typeof nombre);

let edad = 98;  // number
